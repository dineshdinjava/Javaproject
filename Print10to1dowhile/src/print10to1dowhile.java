		
		public class print10to1dowhile {
		    public static void main(String[] args) {
		        int number = 10; // Starting number

		        do {
		            System.out.println(number); // Print the current number
		            number--; // Decrement the number
		        } while (number >= 1); // Loop until the number is greater than or equal to 1
		    }
		}